# Markdown 测试文件

"""
这是一个包含 Markdown 格式的测试文件

## 格式示例

**粗体文本** - 这是加粗的内容

*斜体文本* - 这是斜体的内容

`代码片段` - 这是行内代码

## 列表示例

### 无序列表
- 项目一
- 项目二
- 项目三

### 有序列表
1. 第一步
2. 第二步
3. 第三步

### 混合列表
- 主项目 1
  - 子项目 1.1
  - 子项目 1.2
- 主项目 2
  - 子项目 2.1
"""

def show_markdown():
    """显示 Markdown 格式示例"""
    print("**粗体**")
    print("*斜体*")
    print("`代码`")
    print("\n列表:")
    print("- 项目 1")
    print("- 项目 2")
    print("- 项目 3")

if __name__ == "__main__":
    show_markdown()
