# **粗体注释示例文件**
# 这是一个演示 **Python 粗体注释** 的文件

def greet(name):
    """
    **功能说明**: 向用户打招呼
    
    参数:
        name: 用户名
    
    返回:
        问候语字符串
    """
    # **重要**: 这里处理名字
    message = f"Hello, {name}!"
    return message

def main():
    # **主函数入口**
    print(greet("World"))

if __name__ == "__main__":
    main()
